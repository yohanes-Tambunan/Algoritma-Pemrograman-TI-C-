#1. syntax pada python
#syntax adalah aturan penulisan kode yang dikenal bersih, sederhana, dan mudah dibaca, dengan fokus utama pada penggunaan indentasi (spasi/tab) untuk menentukan blok kode daripada kurung kurawal.
#didalam python, indentasi sangat penting yang mengacu pada spasi di awal baris kode.
if 10 > 5:
    print("Sepuluh lebih besar dari lima")

#2. pernyataan pada python
#pernyataan adalah baris kode yang dieksekusi oleh interpreter python.
print("kamu terlihat manis")
#di python, kita dapat menulis beberapa pernyataan dalam satu baris dengan memisahkannya menggunakan titik koma (;).

#sebagian besar program Python berisi banyak pernyataan.
#Pernyataan-pernyataan tersebut dieksekusi satu per satu, sesuai urutan penulisannya.
print("isi air panas kedalam gelas")
print("masukkan gula")
print("masukkan bubuk teh")
print("dan aduk hingga merata")
#bisa juga dibuat dalam satu baris dengan menggunakan simbol ;
