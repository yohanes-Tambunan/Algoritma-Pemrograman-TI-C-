#1. cara menampilkan teks pada python
#kita dapat menggunakan fungsi print () untuk menampilkan teks atau nilai keluaran
print("hello world!")
print("hello mars!")
#teks harus berada diantara simbol "" ataupun simbol '
print("makanan")
print('makanan')

#jika kita ingin menampilkan nilai keluaran sebaris,kita bisa menggunakan "end"
print("aku suka kamu", end=" ")
print("tapi boong")

